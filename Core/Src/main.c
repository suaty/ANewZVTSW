/**
 * PB1 Button Check
 */
void ButtonCheck(void)
{
	  if(NewZVTVar.PB1Pressed )
	  {

		  if(NewZVTVar.PB1Timer >= KEY_PRS_DLY)
		  {
			  //
			  NewZVTVar.PB1Timer = 0;
			  NewZVTVar.PB1Pressed = 0;

			  switch (NewZVTVar.ConverterStat) {				//Change
			  		case OFF:
			  			HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_SET);
			  			NewZVTVar.ConverterStat = IDLE;
			  			NewZVTVar.LEDTimer = IDLE_DLY;
			  			break;

			  		case IDLE:

			  			NewZVTVar.ConverterStat = STARTUP;
			  			NewZVTVar.LEDTimer = STRTUP_DLY;
			  			break;

			  		case STARTUP:
			  			NewZVTVar.ConverterStat = OPERATE;
			  			HAL_HRTIM_WaveformOutputStart(&hhrtim1, HRTIM_OUTPUT_TC2);
			  			HAL_HRTIM_WaveformCounterStart(&hhrtim1, HRTIM_TIMERID_TIMER_C);
			  			HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_RESET);
			  			NewZVTVar.LEDTimer = 0;
			  			break;

			  		case OPERATE:
						HAL_HRTIM_WaveformOutputStop(&hhrtim1, HRTIM_OUTPUT_TC1 | HRTIM_OUTPUT_TC2);
						HAL_HRTIM_WaveformCounterStop(&hhrtim1, HRTIM_TIMERID_TIMER_C);
			  			NewZVTVar.ConverterStat = IDLE;
			  			NewZVTVar.LEDTimer = IDLE_DLY;
			  			break;

			  		default:
			  			break;
			  }

		  }
	  }



}


